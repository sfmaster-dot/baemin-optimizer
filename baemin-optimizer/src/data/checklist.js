export const SECTIONS = {
  baemin:  BAEMIN_SECTIONS,
  coupang: COUPANG_SECTIONS,
};

export const CHECKLIST = {
  baemin:  BAEMIN_CHECKLIST,
  coupang: COUPANG_CHECKLIST,
};

// SECTIONS count 자동 계산 — 플랫폼별
PLATFORMS.forEach(p => {
  SECTIONS[p.id].forEach(sec => {
    sec.count = CHECKLIST[p.id].filter(i => i.section === sec.id).length;
  });
});

// ──────────────────────────────────
// 헬퍼 함수
// ──────────────────────────────────

export const getSections  = (platform) => SECTIONS[platform] || [];
export const getChecklist = (platform) => CHECKLIST[platform] || [];
export const getItem      = (platform, id) =>
  CHECKLIST[platform]?.find(i => i.id === id);
export const getPlatform  = (id) => PLATFORMS.find(p => p.id === id);

// 플랫폼·섹션별 항목 개수
export const getSectionCount = (platform, sectionId) =>
  CHECKLIST[platform]?.filter(i => i.section === sectionId).length || 0;

// 플랫폼별 전체 항목 개수
export const getTotalCount = (platform) =>
  CHECKLIST[platform]?.length || 0;
